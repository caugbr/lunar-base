<div class="help-content">
    <div class="help-header">
        <h3>
            <x-lucide-help-circle class="lucid-icon" />
            Criar Nova Página
        </h3>
        <p>Use esta tela para criar e configurar uma nova página pública no seu site.</p>
    </div>

    <div class="help-body">
        <ul class="help-list">
            <li>
                <strong>Campos Obrigatórios:</strong>
                Garanta o preenchimento do título, do conteúdo do editor e do slug (URL) para conseguir salvar a página.
            </li>
            <li>
                <strong>URL Amigável (Slug):</strong>
                O endereço é gerado automaticamente a partir do título. Caso queira personalizar, utilize apenas letras minúsculas, números e hifens (evite espaços, acentos ou caracteres especiais).
            </li>
            <li>
                <strong>Rascunho vs. Publicado:</strong>
                Se a página ainda não estiver pronta para o público, altere o status para <em>Rascunho</em> antes de salvar. Páginas com status <em>Publicado</em> ficam imediatamente visíveis no site.
            </li>
            <li>
                <strong>Imagens e Mídias:</strong>
                Você pode inserir mídias diretamente no corpo do texto usando os botões do editor ou definir uma imagem de destaque (capa) na seção de imagem da página.
            </li>
            <li>
                <strong>Hierarquia e Templates:</strong>
                Se aplicável, utilize a opção de "Página mãe" para criar subpáginas organizadas e selecione o template visual que melhor se adapta ao layout desejado.
            </li>
        </ul>
    </div>

    <div class="help-footer">
        <p>
            <x-lucide-info class="lucid-icon" />
            Lembre-se de clicar em <strong>Salvar</strong> para aplicar e persistir suas alterações.
        </p>
    </div>
</div>
