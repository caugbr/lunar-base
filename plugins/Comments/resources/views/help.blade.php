    <h4>Funcionamento Automático</h4>
    <p>
        Este plugin funciona de forma totalmente automatizada. Ele escuta o hook <code>post.footer_end</code> do seu tema público e renderiza a área de comentários completa (formulário + listagem histórica) sem que você precise digitar uma única linha de código.
    </p>

    <h4>Moderação de Spam por Palavras-Chave</h4>
    <p>
        Para proteger o seu site contra envios abusivos de robôs, o plugin possui um filtro de moderação inteligente em tempo de execução. Comentários que contiverem termos suspeitos serão automaticamente salvos com o status de <strong>Pendente</strong> para aprovação prévia.
    </p>
    <p class="code">
        <strong>Termos moderados por padrão:</strong> <code>buy</code>, <code>viagra</code>, <code>free casino</code>, <code>spam</code>, <code>cryptocurrency</code> e <code>click here</code>.
    </p>

    <h4>Integração de Identidades (Avatares)</h4>
    <p>
        Se o plugin de <strong>Avatares (Avatars System)</strong> estiver ativo, a área de listagem substituirá as iniciais geradas do nome do autor pela foto de perfil WebP customizada dele de forma automática, elevando o visual estético da sua comunidade.
    </p>
